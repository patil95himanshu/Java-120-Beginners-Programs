package ForLoop;
/**
* @author -- Himanshu Patil
*/
public class Loop {
	
	public static void main(String args[])
	{
		int x,y;
		
		for(x=10; x>=1; x--)
		{
			System.out.println("Value of x is: " + x);
		}
		
		System.out.println("\n");
		
		for(y=1; y<=10; y++)
		{
			System.out.println("The value of y is: " + y);
		}
	}

}
